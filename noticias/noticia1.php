        <p class="mt-5">
            Al culminar mis estudios secundarios en el Davy College de Cajamarca - Promoción 2006 -, me mudé a Nueva York, EEUU, con el fin de seguir la carrera de Ingenierial Industrial en University at Buffalo.
        </p>
        <p>
            Siendo ya bachiller en Ingeniería, comencé mi experiencia laboral en PepsiCo, 
            donde estuve a cargo de la optimización de las rutas de distribución en Estados 
            Unidos y México. Esto me permitió alcanzar una experiencia laboral considerable, la cual decidí 
            complementar siguiendo la Maestría en Engineering Management en la misma universidad.
        </p>
        <p>
            Al finalizar mis estudios de postgrado, entré a trabajar en la empresa Boeing, como ingeniero industrial a cargo de la optimización de producción de los aviones comerciales 777X, para luego hacerlo en los proyectos de los aviones de combate F-15 y F-A18. El haber desarrollado los conocimientos y habilidades necesarias para estos proyectos, en el Departamento de Defensa de Estados Unidos, abrió una nueva oportunidad laboral, esta vez en Boeing-NASA.
        </p>
        <p>
            El partnership Boeing y Nasa tienen como objetivo desarrollar y construir la nave espacial que transportará astronautas al espacio desde suelo Estadounidense. Mi participación en este proyecto, es asegurarme que diseño, ingeniería y producción estén alineados, para cumplir con los requerimientos de la NASA bajo las premisas de tiempo, costo y calendarización.
            Quisiera dar mi reconocimiento al Davy College y el Programa del Diploma del Bachillerato Internacional, los cuales me permitieron una transición sin sobresaltos entre los sistemas educativos de Perú y Estados Unidos. Sin duda me ayudó a abrir mi mente hacia proyectos internacionales y globales.
        </p>

